package org.dynamislight.api.input;

/**
 * Enum representing key codes used for keyboard input within the engine.
 *
 * Each constant in this enum corresponds to a specific key or control typically found
 * on a standard keyboard. These key codes are used to represent keys pressed or held down
 * during user interactions.
 *
 * The {@code KeyCode} enum is intended to standardize keyboard input representation across
 * different parts of the application, enabling consistent handling of user input events.
 */
public enum KeyCode {
    W,
    A,
    S,
    D,
    Q,
    E,
    SPACE,
    SHIFT,
    CTRL,
    ALT,
    ESC,
    ENTER,
    TAB,
    UP,
    DOWN,
    LEFT,
    RIGHT
}
